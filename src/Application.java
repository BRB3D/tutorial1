public class Application {
    public static void main(String[] args) {
        System.out.println("Hello World");
        int myNumber;
        myNumber = 80;
        System.out.println(myNumber);
        int mySecondNumber = 89;
        System.out.println(mySecondNumber);
//        For values that are smaller you can use 16 bit called short.
        short myShort = 847;
        System.out.println(myShort);
//        For longer values you can use long.
        long myLong = 9797;
        System.out.println(myLong);
//        For extra precision you can use double.
        double myDouble = 7.3243;
        System.out.println(myDouble);
//        Float for smaller decimal numbers you have to put an f at the end.
        float myFloat = 324.3f;
        System.out.println(myFloat);
//        Char type can hold a unicode character and specify it by putting it in single quotes.
        char myChar = 'y';
        System.out.println(myChar);
//        Boolean types can only hold true or false.
        boolean myBoolean = true;
        System.out.println(myBoolean);
//        Byte can only hold 8 bits of data which is a number negative or positive that can go to about 127.
        byte myByte = 127;
        System.out.println(myByte);
    }
}
